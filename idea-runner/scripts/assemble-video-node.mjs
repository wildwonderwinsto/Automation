// Cross-platform replacement for assemble-video.sh.
// No bash, jq, awk, or bc required — pure Node + a direct ffmpeg call.

import fs from 'fs';
import path from 'path';
import { execFileSync } from 'child_process';

/**
 * Converts a decimal seconds value to ASS timestamp format: H:MM:SS.cc
 * (centiseconds, not milliseconds like SRT).
 */
function toAssTime(seconds) {
  const h = Math.floor(seconds / 3600);
  const m = Math.floor((seconds % 3600) / 60);
  const s = Math.floor(seconds % 60);
  const cs = Math.round((seconds % 1) * 100);
  return `${h}:${String(m).padStart(2, '0')}:${String(s).padStart(2, '0')}.${String(cs).padStart(2, '0')}`;
}

/**
 * Parses an SRT timestamp "HH:MM:SS,mmm" into decimal seconds.
 */
function parseSrtTime(timeStr) {
  const [hms, ms] = timeStr.trim().split(',');
  const [h, m, s] = hms.split(':').map(Number);
  return h * 3600 + m * 60 + s + Number(ms) / 1000;
}

/**
 * Converts a CSS hex color (#RRGGBB) to ASS color format (&H00BBGGRR).
 * ASS uses BGR byte order with alpha prefix.
 */
function cssToAssColor(cssHex, alpha = '00') {
  const hex = cssHex.replace('#', '');
  const r = hex.substring(0, 2);
  const g = hex.substring(2, 4);
  const b = hex.substring(4, 6);
  return `&H${alpha}${b}${g}${r}`;
}

/**
 * Generates a full ASS (Advanced SubStation Alpha) subtitle file.
 *
 * Supports 7 animation modes inspired by CapCut/Premiere Pro/captify/PupCaps:
 * - none: instant cut
 * - fade: smooth opacity transition
 * - pop: scale bounce (50% → 108% → 100%) with fade
 * - slide-up: vertical scale reveal from bottom
 * - bounce: elastic multi-stage scale animation
 * - typewriter: karaoke word reveal (transparent → visible)
 * - word-highlight: CapCut's signature — all words visible, active word
 *   highlighted with color + bold (emits per-word Dialogue events)
 *
 * @param {object[]} srtBlocks - parsed SRT blocks [{index, start, end, text}]
 * @param {object} captionStyle - {fontFamily, fontSize, position, transition, highlightColor}
 * @param {object[]|null} wordTimings - word-level timing data [{word, start, end}]
 * @returns {string} ASS file content
 */
function generateAssFile(srtBlocks, captionStyle, wordTimings = null) {
  // ── Style parameters ──
  // We always author at 1920x1080 PlayRes. libass auto-scales to any output
  // resolution, so a FontSize of 72 at PlayRes 1920x1080 looks identical
  // whether the output is 1080p or 4K.
  const PLAY_RES_X = 1920;
  const PLAY_RES_Y = 1080;

  const fontName = captionStyle?.fontFamily || 'Arial';

  const fontSizeMap = { small: 48, medium: 72, large: 96 };
  const fontSize = fontSizeMap[captionStyle?.fontSize] || fontSizeMap.medium;

  // ASS Alignment: 1-9 numpad layout
  // 8 = top-center, 5 = middle-center, 2 = bottom-center
  const alignmentMap = { top: 8, center: 5, bottom: 2 };
  const alignment = alignmentMap[captionStyle?.position] || 2;

  // MarginV: percentage of PlayResY to match CSS cqw/cqh units.
  const marginVMap = { top: 38, center: 0, bottom: 38 };
  const marginV = marginVMap[captionStyle?.position] ?? 38;

  // Left/Right margin for text wrapping (matches CSS max-w-[85%])
  const marginLR = 144;

  // Outline and Shadow proportional to font size
  const outline = Math.max(1, Math.round(fontSize * 0.055));
  const shadow = Math.max(1, Math.round(fontSize * 0.025));

  // Colors (ASS uses &HAABBGGRR format)
  const primaryColor = '&H00FFFFFF';    // white, fully opaque
  const outlineColor = '&H00000000';    // black outline
  const backColor = '&H80000000';       // semi-transparent black shadow

  const transition = captionStyle?.transition || 'none';

  // For typewriter mode: SecondaryColour is fully transparent so \kf sweeps
  // from invisible → visible (the text "writes in").
  // For word-highlight: SecondaryColour is the highlight color.
  // For all others: SecondaryColour is red (standard ASS default, unused).
  let secondaryColor;
  if (transition === 'typewriter') {
    secondaryColor = '&HFF000000'; // fully transparent (alpha=FF)
  } else if (transition === 'word-highlight') {
    const hlColor = captionStyle?.highlightColor || '#FFD700';
    secondaryColor = cssToAssColor(hlColor);
  } else {
    secondaryColor = '&H000000FF'; // red (unused in non-karaoke modes)
  }

  // ── Build ASS header ──
  let ass = '';

  // [Script Info]
  ass += `[Script Info]\n`;
  ass += `; Generated by idea-runner (CapCut-style captions engine)\n`;
  ass += `Title: Auto Captions\n`;
  ass += `ScriptType: v4.00+\n`;
  ass += `WrapStyle: 0\n`;
  ass += `PlayResX: ${PLAY_RES_X}\n`;
  ass += `PlayResY: ${PLAY_RES_Y}\n`;
  ass += `ScaledBorderAndShadow: yes\n`;
  ass += `\n`;

  // [V4+ Styles]
  ass += `[V4+ Styles]\n`;
  ass += `Format: Name, Fontname, Fontsize, PrimaryColour, SecondaryColour, OutlineColour, BackColour, Bold, Italic, Underline, StrikeOut, ScaleX, ScaleY, Spacing, Angle, BorderStyle, Outline, Shadow, Alignment, MarginL, MarginR, MarginV, Encoding\n`;
  ass += `Style: Default,${fontName},${fontSize},${primaryColor},${secondaryColor},${outlineColor},${backColor},0,0,0,0,100,100,0,0,1,${outline},${shadow},${alignment},${marginLR},${marginLR},${marginV},1\n`;

  // Bold variant for word-highlight active word
  if (transition === 'word-highlight') {
    const hlColor = captionStyle?.highlightColor || '#FFD700';
    const highlightAssColor = cssToAssColor(hlColor);
    ass += `Style: Highlight,${fontName},${fontSize},${highlightAssColor},${secondaryColor},${outlineColor},${backColor},1,0,0,0,100,100,0,0,1,${outline},${shadow},${alignment},${marginLR},${marginLR},${marginV},1\n`;
  }
  ass += `\n`;

  // [Events]
  ass += `[Events]\n`;
  ass += `Format: Layer, Start, End, Style, Name, MarginL, MarginR, MarginV, Effect, Text\n`;

  for (const block of srtBlocks) {
    const blockStart = parseSrtTime(block.start);
    const blockEnd = parseSrtTime(block.end);

    if (transition === 'word-highlight') {
      // ── Word Highlight Mode ──
      // Emit per-word Dialogue lines. Each line shows the full caption text,
      // but the currently active word uses the Highlight style color + bold.
      // This is exactly how CapCut and PupCaps implement word-by-word highlighting.
      const lines = buildWordHighlightLines(block, wordTimings, captionStyle);
      for (const line of lines) {
        ass += line + '\n';
      }
    } else {
      // ── Standard single-line modes ──
      const startTime = toAssTime(blockStart);
      const endTime = toAssTime(blockEnd);
      let text = block.text.replace(/\n/g, '\\N');
      text = applyTransitionTags(text, transition, block, wordTimings);
      ass += `Dialogue: 0,${startTime},${endTime},Default,,0,0,0,,${text}\n`;
    }
  }

  return ass;
}

/**
 * Builds per-word Dialogue lines for the word-highlight effect.
 *
 * For a caption "Today we explore" with word timings:
 *   - "Today" active 1.00–1.30
 *   - "we" active 1.30–1.50
 *   - "explore" active 1.50–2.00
 *
 * Emits 3 Dialogue lines, each showing full text with a different word
 * highlighted using ASS inline color overrides:
 *
 *   Dialogue: 0,0:00:01.00,0:00:01.30,Default,,0,0,0,,{\1c&H00D7FF&\b1}Today {\1c&HFFFFFF&\b0}we explore
 *   Dialogue: 0,0:00:01.30,0:00:01.50,Default,,0,0,0,,{\1c&HFFFFFF&}Today {\1c&H00D7FF&\b1}we {\1c&HFFFFFF&\b0}explore
 *   Dialogue: 0,0:00:01.50,0:00:02.00,Default,,0,0,0,,{\1c&HFFFFFF&}Today we {\1c&H00D7FF&\b1}explore
 */
function buildWordHighlightLines(block, wordTimings, captionStyle) {
  const blockStart = parseSrtTime(block.start);
  const blockEnd = parseSrtTime(block.end);
  const hlColor = captionStyle?.highlightColor || '#FFD700';
  const highlightAss = cssToAssColor(hlColor);
  const whiteAss = '&H00FFFFFF';

  // Find word timings within this block's time range
  const blockWords = wordTimings
    ? wordTimings.filter(w => w.start >= blockStart - 0.05 && w.end <= blockEnd + 0.05)
    : [];

  if (blockWords.length === 0) {
    // No word timings — fall back to showing full text for the entire block
    // with a simple fade
    return [
      `Dialogue: 0,${toAssTime(blockStart)},${toAssTime(blockEnd)},Default,,0,0,0,,{\\fad(150,150)}${block.text.replace(/\n/g, '\\N')}`
    ];
  }

  const lines = [];

  for (let i = 0; i < blockWords.length; i++) {
    const wordStart = blockWords[i].start;
    // Each word's highlight lasts until the next word starts (or block ends)
    const wordEnd = i < blockWords.length - 1
      ? blockWords[i + 1].start
      : blockEnd;

    // Skip zero-duration segments
    if (wordEnd <= wordStart) continue;

    // Build the full text line with inline ASS color overrides
    let lineText = '';
    for (let j = 0; j < blockWords.length; j++) {
      const sep = j > 0 ? ' ' : '';
      if (j === i) {
        // Active word: highlight color + bold + subtle scale up
        lineText += `${sep}{\\1c${highlightAss}\\b1\\fscx105\\fscy105}${blockWords[j].word}{\\fscx100\\fscy100}`;
      } else {
        // Inactive word: white + normal weight
        lineText += `${sep}{\\1c${whiteAss}\\b0}${blockWords[j].word}`;
      }
    }

    // Add a subtle fade-in on the first word of each block
    const fadePrefix = i === 0 ? '{\\fad(120,0)}' : '';

    lines.push(
      `Dialogue: 0,${toAssTime(wordStart)},${toAssTime(wordEnd)},Default,,0,0,0,,${fadePrefix}${lineText}`
    );
  }

  return lines;
}

/**
 * Applies ASS override tags for the selected transition/animation effect.
 * These are improved versions inspired by captify and CapCut's actual animation curves.
 */
function applyTransitionTags(text, transition, block, wordTimings) {
  switch (transition) {
    case 'fade':
      // Smooth fade in (200ms) and out (200ms) — simple and reliable
      return `{\\fad(200,200)}${text}`;

    case 'pop':
      // CapCut-style pop: scale from 50% → overshoot to 108% → settle at 100%
      // Two-stage \t animation with a smooth ease-out curve.
      // Combined with a quick 100ms fade-in to avoid the jarring first frame.
      return `{\\fad(100,0)\\fscx50\\fscy50\\t(0,180,1,\\fscx108\\fscy108)\\t(180,300,1,\\fscx100\\fscy100)}${text}`;

    case 'slide-up':
      // Slide-up reveal: starts vertically collapsed (fscy=20%) and grows to full height
      // combined with fade. This simulates upward motion since ASS can't animate
      // position with \t when using alignment-based placement.
      return `{\\fad(150,100)\\fscy20\\t(0,280,1,\\fscy100)}${text}`;

    case 'bounce':
      // Elastic bounce: three-stage scale animation (small → overshoot → undershoot → settle)
      // Mimics a spring/elastic easing curve that CapCut uses for its "bounce" preset.
      return `{\\fad(80,0)\\fscx65\\fscy65\\t(0,140,1,\\fscx112\\fscy112)\\t(140,230,1,\\fscx95\\fscy95)\\t(230,330,1,\\fscx100\\fscy100)}${text}`;

    case 'typewriter': {
      // Karaoke word-by-word reveal using \kf (karaoke fill) tags.
      // SecondaryColour is set to fully transparent in the style header,
      // so \kf sweeps each word from invisible → visible, creating a
      // smooth "write-in" effect. This is the core technique behind
      // captify's word-reveal and CapCut's typewriter preset.
      //
      // Prefix with \2a&HFF& to ensure secondary alpha is transparent
      // even if someone changed the style.
      if (wordTimings && wordTimings.length > 0) {
        return buildKaraokeLine(text, block, wordTimings);
      }
      return buildEstimatedKaraoke(text, block);
    }

    case 'none':
    default:
      return text;
  }
}

/**
 * Builds a karaoke line using actual word-level timestamps from Whisper.
 * Uses \kf (karaoke fill) tags — the same smooth sweep CapCut uses.
 * Prefixes with transparent secondary alpha so text sweeps from invisible → white.
 *
 * Format: {\2a&HFF&}{\kf<duration_in_centiseconds>}Word
 */
function buildKaraokeLine(text, block, wordTimings) {
  const blockStart = parseSrtTime(block.start);
  const blockEnd = parseSrtTime(block.end);

  // Find word timings that fall within this block's time range
  const blockWords = wordTimings.filter(w =>
    w.start >= blockStart - 0.05 && w.end <= blockEnd + 0.05
  );

  if (blockWords.length === 0) {
    return buildEstimatedKaraoke(text, block);
  }

  // Start with transparent secondary color override
  let karaokeLine = '{\\2a&HFF&}';

  // Add initial delay from block start to first word
  if (blockWords[0].start > blockStart) {
    const delayCs = Math.round((blockWords[0].start - blockStart) * 100);
    if (delayCs > 0) {
      karaokeLine += `{\\kf${delayCs}}`;
    }
  }

  for (let i = 0; i < blockWords.length; i++) {
    const w = blockWords[i];
    const durationCs = Math.max(1, Math.round((w.end - w.start) * 100));
    const separator = i > 0 ? ' ' : '';
    karaokeLine += `${separator}{\\kf${durationCs}}${w.word}`;
  }

  return karaokeLine;
}

/**
 * Fallback karaoke when word timings aren't available.
 * Splits text into words and distributes timing evenly.
 */
function buildEstimatedKaraoke(text, block) {
  const blockStart = parseSrtTime(block.start);
  const blockEnd = parseSrtTime(block.end);
  const totalDurationCs = Math.round((blockEnd - blockStart) * 100);

  const words = text.split(/\s+/).filter(w => w.length > 0);
  if (words.length === 0) return text;

  const perWordCs = Math.max(1, Math.round(totalDurationCs / words.length));
  let karaokeLine = '{\\2a&HFF&}';

  for (let i = 0; i < words.length; i++) {
    const separator = i > 0 ? ' ' : '';
    karaokeLine += `${separator}{\\kf${perWordCs}}${words[i]}`;
  }

  return karaokeLine;
}

/**
 * Assembles final_video.mp4 inside projectDir from scenes.json,
 * voiceover.mp3, and captions.srt.
 *
 * Image durations are sourced from scene_timings.json (generated by
 * generate-captions.mjs) which gives exact Whisper-aligned start/end
 * times per scene.  Falls back to parsing captions.srt if the timings
 * file is missing (legacy path).
 *
 * @param {string} projectDir
 * @param {string} ffmpegPath - resolved path to the ffmpeg binary (e.g. from ffmpeg-static)
 * @param {string} resolution
 * @param {number} fps
 * @param {object|null} captionStyle
 * @param {boolean} includeCaptions
 * @returns {string} path to the generated final_video.mp4
 */
export function assembleVideo(projectDir, ffmpegPath, resolution = "1080p", fps = 30, captionStyle = null, includeCaptions = true) {
  const scenesJsonPath = path.join(projectDir, 'scenes.json');
  const audioPath = path.join(projectDir, 'voiceover.mp3');
  const srtPath = path.join(projectDir, 'captions.srt');
  const sceneTimingsPath = path.join(projectDir, 'scene_timings.json');
  const wordTimingsPath = path.join(projectDir, 'word_timings.json');
  const assPath = path.join(projectDir, 'captions.ass');
  const outputPath = path.join(projectDir, 'final_video.mp4');

  for (const f of [scenesJsonPath, audioPath, srtPath]) {
    if (!fs.existsSync(f)) {
      throw new Error(`Missing required file: ${f}`);
    }
  }

  const scenes = JSON.parse(fs.readFileSync(scenesJsonPath, 'utf-8'));

  if (scenes.length === 0) {
    throw new Error('scenes.json has no scenes');
  }

  // ── Resolve per-scene durations ──
  let sceneTimings = null;
  if (fs.existsSync(sceneTimingsPath)) {
    sceneTimings = JSON.parse(fs.readFileSync(sceneTimingsPath, 'utf-8'));
  }

  function getSceneDuration(sceneIndex) {
    if (sceneTimings && sceneTimings[sceneIndex]) {
      const t = sceneTimings[sceneIndex];
      return Math.max(0.1, t.end - t.start);
    }
    // Legacy fallback: parse captions.srt
    const srtContent = fs.readFileSync(srtPath, 'utf-8');
    const blocks = srtContent.trim().split(/\r?\n\r?\n/);
    for (const block of blocks) {
      const lines = block.split(/\r?\n/);
      if (parseInt(lines[0], 10) === sceneIndex + 1) {
        const [startStr, endStr] = lines[1].split('-->');
        const start = parseSrtTime(startStr);
        const end = parseSrtTime(endStr);
        return Math.max(0.1, end - start);
      }
    }
    return 3; // ultimate fallback: 3 seconds
  }

  const inputArgs = [];
  const filterParts = [];
  const concatLabels = [];
  
  const is4K = resolution === "4k";
  const targetWidth = is4K ? 3840 : 1920;
  const targetHeight = is4K ? 2160 : 1080;

  scenes.forEach((scene, i) => {
    const imagePath = scene.selected_image;
    if (!imagePath || !fs.existsSync(imagePath)) {
      throw new Error(`Image not found for scene ${i + 1}: ${imagePath}`);
    }

    const duration = getSceneDuration(i);

    inputArgs.push('-loop', '1', '-t', String(duration), '-i', imagePath);
    filterParts.push(
      `[${i}:v]scale=${targetWidth}:${targetHeight}:force_original_aspect_ratio=decrease,pad=${targetWidth}:${targetHeight}:(ow-iw)/2:(oh-ih)/2:color=black,setsar=1,fps=${fps}[v${i}]`
    );
    concatLabels.push(`[v${i}]`);
  });

  const audioIndex = scenes.length;

  // ── Generate ASS subtitle file ──
  // Parse SRT blocks
  const rawSrt = fs.readFileSync(srtPath, 'utf-8');
  const srtBlocks = [];
  const chunks = rawSrt.trim().split(/\r?\n\r?\n/);
  for (const chunk of chunks) {
    const lines = chunk.split(/\r?\n/);
    if (lines.length >= 3) {
      const index = parseInt(lines[0], 10);
      const [start, end] = lines[1].split(' --> ');
      const text = lines.slice(2).join('\n');
      srtBlocks.push({ index, start: start.trim(), end: end.trim(), text });
    }
  }

  // Load word-level timings if available (for karaoke + word-highlight modes)
  let wordTimings = null;
  if (fs.existsSync(wordTimingsPath)) {
    try {
      wordTimings = JSON.parse(fs.readFileSync(wordTimingsPath, 'utf-8'));
      console.log(`   ✓ Loaded word_timings.json (${wordTimings.length} words)`);
    } catch (e) {
      console.warn('Warning: could not parse word_timings.json, karaoke/highlight modes will use estimated timing');
    }
  } else {
    console.warn('   ⚠ word_timings.json not found — karaoke/highlight will use estimated timing');
  }

  // Generate the ASS file
  if (includeCaptions) {
    const assContent = generateAssFile(srtBlocks, captionStyle, wordTimings);
    fs.writeFileSync(assPath, assContent);
    console.log(`   ✓ Generated ASS subtitle file: ${assPath}`);
  }

  // Build the full filter_complex graph:
  // 1. Scale/pad each image → [v0], [v1], ...
  // 2. Concat all segments  → [raw]
  // 3. Burn in ASS subtitles → [video]
  //
  // Using ass= filter instead of subtitles= because:
  // - ass= reads the full ASS file with styles baked in
  // - No force_style needed — everything is in the ASS header
  // - PlayResX/PlayResY handles resolution scaling automatically
  // We run ffmpeg inside projectDir to avoid escaping issues with C: drive colons
  let filterComplex =
    filterParts.join(';') +
    `;${concatLabels.join('')}concat=n=${scenes.length}:v=1:a=0[raw]`;

  if (includeCaptions) {
    filterComplex += `;[raw]ass='captions.ass'[video]`;
  }

  const args = [
    '-y',
    ...inputArgs,
    '-i', audioPath,
    '-filter_complex', filterComplex,
    '-map', includeCaptions ? '[video]' : '[raw]',
    '-map', `${audioIndex}:a`,
    '-c:v', 'libx264', '-preset', 'medium', '-crf', '23',
    '-pix_fmt', 'yuv420p',
    '-c:a', 'aac', '-b:a', '192k',
    outputPath,
  ];

  execFileSync(ffmpegPath, args, { stdio: 'inherit', cwd: projectDir });

  return outputPath;
}